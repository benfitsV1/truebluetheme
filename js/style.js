$(".slide-select").click(function () {
    $(".slide").removeClass("top-slide");
    var slideIndex = $(".slide-select").index(this);
    var c;
    i = slideIndex;
    if (i === $(".slide-select").length){
      c = 0;
    } else {
      c = i + 1; 
    }
    var $top = $(".slide")[c];
    $($top).addClass("top-slide");
    slideChange();
  });


  

